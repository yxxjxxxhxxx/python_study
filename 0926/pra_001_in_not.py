# in, not in
a = [1, 2, 3, 4]

b = "apple"

print("a=", a)

print("b=", b)

print()

print("3 in a : ", 3 in a)

print("a in b : ", "a" in b)

print("5 not in a : ", 5 not in a)

print("z not in b : ", "z" not in b)

print()

print("13 in a : ", 13 in a)

print("app in b : ", "app" in b)

print("2 not in a : ", 2 not in a)

print("appp not in b : ", "appp" not in b)